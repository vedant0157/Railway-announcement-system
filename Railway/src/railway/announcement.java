       private void sendAnnouncement(String trainNumber, String platformNumber, String trainTime) throws Exception {
        String url = "http://localhost:3000/api/train/trainNumber";
        URL obj = new URL(url);
        HttpURLConnection con = (HttpURLConnection) obj.openConnection();

        // Add request header
        con.setRequestMethod("GET");
        con.setRequestProperty("User-Agent", "Mozilla/5.0");
        con.setRequestProperty("Content-Type", "application/json");

        String postData = String.format(
                "{\"trainNumber\":\"%s\", \"platformNumber\":\"%s\", \"trainTime\":\"%s\"}",
                trainNumber, platformNumber, trainTime);
        con.setDoOutput(true);
        try (DataOutputStream wr = new DataOutputStream(con.getOutputStream())) {
            wr.writeBytes(postData);
            wr.flush();
        }

        // Read response
        try (BufferedReader in = new BufferedReader(new InputStreamReader(con.getInputStream()))) {
            StringBuilder response = new StringBuilder();
            String inputLine;
            while ((inputLine = in.readLine()) != null) {
                response.append(inputLine);
            }
            System.out.println(response.toString());
        }
    }
